public class donguContinueFor {
    public static void main(String[] args) {
       for(int i=0;i<10;i++){
           
           
           if(i==8){
               continue;//koşuldaki değeri pas geçmek için continue kullanırız
           }
           System.out.println(i);
       }
        
    }
}
